                <a href="#panel1">I have never taken an online course before – what should I expect?</a>
                <div id="panel1" class="content">
                  An online course runs similarly to an in-class course, except your classroom is a website on the internet. You have a teacher, classmates, assignments, tests and deadlines. If you want to experience the online learning environment before you register, check out the demo course available on this site.
                </div>
              </dd>
              <dd>
                <a href="#panel2">Are Humber's online courses self-paced?</a>
                <div id="panel2" class="content">
                  No, online courses at Humber have a set start and end date. While you can access your course material anytime, you are expected to complete the course requirements as outlined by your teacher in the critical path. You may notice that some online courses associate a set day and time for the course. This is simply to accommodate our registration system. You can access your site anytime after the start date, providing you register before the course starts.
                </div>
              </dd>
              <dd>
                <a href="#panel3">When do I get access to my course site?</a>
                <div id="panel3" class="content">
                  You will get access to the course on or just prior to your course start date (usually 2-3 days before). If you register after the course has started, you will access 24 hours after you register. If you register on a Friday, you will get access the following Monday. To ensure no delay at start-up - Register Early!!!
                </div>
              </dd>
              <dd>
                <a href="#panel4">How do I get my username and password?</a>
                <div id="panel4" class="content">
                  Humber offers a number of online courses on a number of learning systems. It is very important to visit your course information page on the OLC website to confirm your login instructions. We strongly recommend that your change your password after your gain access to your course site.
                </div>
              </dd>
              <dd>
                <a href="#panel5">Do I have to buy a book for an online course?</a>
                <div id="panel5" class="content">
                  Some courses require a text book. To confirm if your course requires a textbook, visit the Humber Bookstore website.
                </div>
              </dd>
              <dd>
                <a href="#panel6">Who do I contact if I need help with my online course?</a>
                <div id="panel6" class="content">
                  There are a number of support areas to help you have a positive learning experience. If you have questions about: <br />
                  <span class="bold">Course Content:</span> Your Instructor or your classmates within your course site<br />
                  <span class="bold">Accessing/Using the Learning System:</span> The Open Learning Centre (see Contact Us at the top of this page)<br />
                  <span class="bold">Technical Assistance:</span> Humber's IT Customer Care Department (<a href="http://www.its.humber.ca" target="_blank">www.its.humber.ca</a>)
                </div>
              </dd>
              <dd>
                <a href="#panel7">Is there a final exam in online courses?</a>
                <div id="panel7" class="content">
                  Yes, credit courses have a final exam. Your instructor will confirm the details in your course site. If you can not come to Humber to complete your final exam, the OLC will be happy to assist you in making the arrangements at another educational institution - with your instructor's approval of course.
                </div>
              </dd>
              <dd>
                <a href="#panel8">Can I see the course outline before the course starts?</a>
                <div id="panel8" class="content">
                  Course outlines are available through the department that offers the course, so you will have to contact the division to make this request.
                </div>
              </dd>
              <dd>
                <a href="#panel9">How do I get my certificate?</a>
                <div id="panel9" class="content">
                  Once you have successfully met the program requirements (may include a number of courses), you must contact the registration office to request your certificate.
                </div>
              </dd>
              <dd>
                <a href="#panel10">What are some tips for success in Online Learning?</a>
                <div id="panel10" class="content">
                  <ul>
                    <li>Paticipate:
                      <ul>
                        <li>Stay in contact with your teacher and your classmates through the discussion board and other activities.  Participation often contributes to your final grade.</li>
                      </ul>
                    </li>
                    <li>Know your deadlines:
                      <ul>
                        <li>Most online courses at Humber are NOT self-paced.  Look for a critical path or a table of deadlines when you first login to your course site.</li>
                        <li>If you feel you can’t meet a deadline, let your teacher know as soon as possible – don’t wait until the last minute as it is often harder to accommodate your request then.</li>
                      </ul>
                    </li>
                    <li>Be patient:
                      <ul>
                        <li>You have access to the course site 24x7, but remember your teacher and classmates are not sitting at the other end waiting for your email or discussion posting. Give them time to reply.  Usually, you can expect a response within 48 hours or sooner.</li>
                      </ul>
                    </li>
                    <li>Be organized:
                      <ul>
                        <li>Manage your time appropriately.  As a guideline, you should give yourself about 2-3 hours per week to work through the course content and another 3-4 hours per week to complete assignments and participate in discussions and online activities.</li>
                      </ul>
                    </li>
                    <li>Read
                      <ul>
                        <li>It is very easy to skim information on the web, but it is important that you read through the material in your course site to ensure a positive learning experience.</li>
                      </ul>
                    </li>
                  </ul>
                </div>
              </dd>
